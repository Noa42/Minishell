#include "libft.h"

t_stack	*ft_lstlast(t_stack *lst)
{
	t_stack	*ptr;

	ptr = lst;
	if (lst == NULL)
		return (NULL);
	while (ptr -> next)
	{
		ptr = ptr -> next;
	}
	return (ptr);
}
